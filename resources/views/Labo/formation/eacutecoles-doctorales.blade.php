<img src="../clear.gif" width="1" height="10" border="0" class="spacer-gif" alt="" title="" /><br /><a id="c209"></a><p style="margin:0 0 0 0;">GIPSA-lab accueille tous les ans une trentaine de nouveaux doctorants issus des filières d'enseignement de Grenoble, d'autres universités françaises ou d'universités étrangères (25 %).
</p>
<p style="margin:0 0 0 0;"><br />Le laboratoire est reconnu par l'école doctorale <span style="font-weight: bold;"><a href="http://edeeats.grenoble-inp.fr/" target="_blank" >EEATS</a></span> (Electronique, Electrotechnique Automatique et Traitement du signal). A ce titre, il accueille les doctorants inscrits dans cette école et bénéficie des allocations de recherche ministérielles pour les spécialités <span style="font-weight: bold;">Signal, Image, Parole, Télécoms</span> (SIPT) et <span style="font-weight: bold;">Automatique Productique</span>.<br /><br />Le laboratoire (selon les équipes) est également reconnu par les écoles doctorales suivantes pour accueillir des doctorants :<br /><br />&nbsp;&nbsp;&nbsp; <span style="font-weight: bold;"><a href="http://www-sante.ujf-grenoble.fr/edisce/" target="_blank" >EDISCE</a></span>, pour les sciences cognitives<br />&nbsp;&nbsp;&nbsp; <span style="font-weight: bold;"><a href="http://www.u-grenoble3.fr/version-francaise/recherche-valorisation/ecole-doctorale/ecole-doctorale-langues-litteratures-et-sciences-humaines-74019.kjsp" target="_blank" >LLSH</a></span>, pour les sciences du langage<br />&nbsp;&nbsp;&nbsp; <span style="font-weight: bold;"><a href="http://edmstii.ujf-grenoble.fr/" target="_blank" >MSTII</a></span>, pour les sciences de l'information<br />&nbsp;&nbsp;&nbsp; <span style="font-weight: bold;"><a href="http://edimep2.grenoble-inp.fr/" target="_blank" >I-MEP2</a></span>, pour les systèmes de production,<br />&nbsp;&nbsp;&nbsp; <span style="font-weight: bold;"><a href="http://www.obs.ujf-grenoble.fr/ecole_doctorale/" target="_blank" >TUE</a></span>, pour la géophysique et la sismique<br /><br />Plus d'informations sur les écoles doctorales : site de l'<span style="font-weight: bold;"><a href="http://www.grenoble-univ.fr/ecolesdoctorales/" target="_blank" >Université de Grenoble</a></span>
</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;"><h2>Comment faire une thèse à GIPSA-lab ?</h2>
</p>
<p style="margin:0 0 0 0;">Vous êtes intéressé par les recherches scientifiques du laboratoire et vous souhaitez poser votre candidature au laboratoire : consultez la liste des <span style="font-weight: bold;"><a href="../offres/offres_de_theses.html" >offres de thèses</a></span> et prenez contact directement avec le responsable indiqué dans la description de l'offre.<br />Si votre candidature est retenue, vous devrez ensuite vous inscrire à l'école doctorale correspondant à la spécialité de cette thèse.
</p>
<p style="margin:0 0 0 0;"> GIPSA-lab ne peut accueillir des doctorants dont la thèse ne serait pas financée. Renseignez-vous auprès de l'école doctorale de votre spécialité.</p><img src="../clear.gif" width="1" height="10" border="0" class="spacer-gif" alt="" title="" /><br /> 
