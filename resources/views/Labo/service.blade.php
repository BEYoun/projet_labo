		<a id="c202"></a><p class="contenu_entete" style="margin:0 0 0 0;">Services administratifs et techniques</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">GIPSA-lab possède des services administratifs et techniques mutualisés sur l'ensemble du laboratoire. 
</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">Le service <span style="font-weight: bold;">Administratif et financier</span> est dirigé par Akila MOKHTARI, responsable administrative de GIPSA-lab. Il est structuré en 2 cellules :
</p>
<p class="indent" style="margin:0 0 0 0;"><ul><li>Le <span style="font-weight: bold"><a href="services-administratifs-et-techniques/ressources-humaines.html" target="_self" >service Ressources humaines</a></span> assiste le directeur pour tout ce qui concerne la gestion des personnels.<br /><br /></li><li>Le <a href="services-administratifs-et-techniques/financier.html" target="_self" ><span style="font-weight: bold">service Financier</span></a> est chargé de mettre en place les crédits du laboratoire et d'en assurer la gestion en conformité avec les règles comptables des établissements de rattachement. </li></ul></p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">Le <span style="font-weight: bold"><a href="services-administratifs-et-techniques/documentation.html" target="_self" >service Documentation</a></span> assure l'accès aux fonds documentaires et aux ressources électroniques proposés dans les bibliothèques et contre de documentation du laboratoire. Il valorise et diffuse la production scientifique de l'unité.
</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">Le <span style="font-weight: bold"><a href="services-administratifs-et-techniques/plates-formes-et-projets.html">service Plates-formes et projets</a></span> assure le support des plates-formes expérimentales du laboratoire : conception, mise en place matérielle et logicielle, traitement et diffusion des données, valorisation, mise à disposition et formation des utilisateurs, ainsi que l'appui aux projets scientifiques (conception et développement de logiciels, encadrement de doctorants ou stagiaires) ; il assure également le développement de Systèmes de gestion du laboratoire (système d'information et outils de pilotage et de communication). 
</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">Le <span style="font-weight: bold"><a href="services-administratifs-et-techniques/communication.html" target="_self" >service Communication</a></span> est chargé de définir et mettre en oeuvre la politique de communication du laboratoire afin de valoriser les activités et les résultats scientifiques du laboratoire.
</p>
<p style="margin:0 0 0 0;">&nbsp;</p>
<p style="margin:0 0 0 0;">Le <span style="font-weight: bold"><a href="services-administratifs-et-techniques/informatique.html" >service Informatique</a></span> met en œuvre les infrastructures informatiques répondant aux besoins des personnels du laboratoire. Il veille à la bonne marche des serveurs et des services réseau, assure l’exploitation quotidienne d’un parc de plus de 660 machines, et offre conseil et assistance aux utilisateurs de l’informatique répartie.</p><img src="../clear.gif" width="1" height="10" border="0" class="spacer-gif" alt="" title="" /><br /> 
